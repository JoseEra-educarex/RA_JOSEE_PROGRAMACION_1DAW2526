package unidad1;

public class Aritmetica {

	public static void main(String[] args) {
		// Declara dos variables x = 15 y y = 4.
		//Muestra el resultado de x + y, x - y, x * y, x / y y x % y.
		
	int	x = 15, y = 4;
	System.out.println("x = 15; y = 4");
	System.out.println("x + y = " + (x+y));
	System.out.println("x - y = " + (x-y));
	System.out.println("x * y = " + (x*y));
	System.out.println("x % y = " + (x%y));
	}

}
